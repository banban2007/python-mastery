print('isalnum')
print("Hello","Hello".isalnum())
print("He  o","He  o".isalnum())

# The isalnum () method returns True if all the characters are alphanumeric,
# meaning alphabet letter (a-z) and numbers (0-9).
# Example of characters that are not alphanumeric: (space) !#%&? etc.

print('isalpha')
print("Hello","Hello".isalpha())
print("He123","He123".isalpha())

# The isalpha() method returns True if all the characters are alphabet letters (a-z)

print('isnumeric')
print("1234","1234".isnumeric())
print("He123","He123".isnumeric())

# The isnumeric() method returns True if all the characters are numeric (0-9), otherwise False.
